public class ProtocolMessage {

    private int req;
    private String type;

    public int getReq() {
        return req;
    }

    public void setReq(int req) {
        this.req = req;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
